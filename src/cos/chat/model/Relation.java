package cos.chat.model;

public class Relation {
	private int id;
	private int manID;
	private int womanID;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getManID() {
		return manID;
	}
	public void setManID(int manID) {
		this.manID = manID;
	}
	public int getWomanID() {
		return womanID;
	}
	public void setWomanID(int womanID) {
		this.womanID = womanID;
	}

}
